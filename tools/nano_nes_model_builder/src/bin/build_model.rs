//! Build the NanoCamelid NES word-trigram chat model.
//!
//! Usage: build_model <corpus.txt> <routes.txt> <topics.txt> <questions.txt> <out_dir>
//!
//! Emits into <out_dir>:
//!   model.bin        header + trigram + bigram + word table (reference form)
//!   tri.bin          1,024,000 B dense trigram table (125 MMC5 8 KiB banks)
//!   bi.bin           4,000 B bigram fallback table (decode bank)
//!   words.bin        word offset tables + strings (fixed bank)
//!   chr.bin          8 KiB CHR-ROM font bank (tile == char ID)
//!   model_consts.inc ca65 include: vocab size + punctuation word IDs
//!   model_receipt.json

use nano_nes_model_builder::*;
use std::fs;
use std::path::Path;

fn main() -> anyhow::Result<()> {
    let args: Vec<String> = std::env::args().collect();
    anyhow::ensure!(args.len() == 6, "usage: build_model <corpus.txt> <routes.txt> <topics.txt> <questions.txt> <out_dir>");
    let corpus_path = Path::new(&args[1]);
    let routes_path = Path::new(&args[2]);
    let topics_path = Path::new(&args[3]);
    let questions_path = Path::new(&args[4]);
    let out_dir = Path::new(&args[5]);
    fs::create_dir_all(out_dir)?;

    let corpus_bytes = fs::read(corpus_path)?;
    let corpus_text = String::from_utf8(corpus_bytes.clone())?;
    let routes_text = fs::read_to_string(routes_path)?;
    let topics_text = fs::read_to_string(topics_path)?;
    let (model, n_tri, n_bi) = build_model(&corpus_text, &routes_text, &topics_text)?;
    let model_bytes = model.to_bytes();
    let words_bin = model.vocab.to_bytes();

    // Every verification question must produce a non-empty answer.
    let questions_text = fs::read_to_string(questions_path)?;
    let questions: Vec<&str> = questions_text.lines().filter(|l| !l.trim().is_empty()).collect();
    for q in &questions {
        let t = answer_pipeline(&model, q);
        anyhow::ensure!(
            t.words.len() >= MIN_ANSWER_WORDS,
            "question {q:?} answers with only {} words",
            t.words.len()
        );
    }

    let id_or = |w: &str| model.vocab.id(w).unwrap_or(0xFE);
    let consts = format!(
        "; generated by build_model — word IDs depend on the corpus\n\
         MODEL_VOCAB_SIZE = {}\n\
         WORD_DOT   = {}\nWORD_COMMA = {}\nWORD_EXCL  = {}\nWORD_QM    = {}\n\
         WORD_QMARK_SEED = {}\nWORD_BUDGET = {}\nMIN_WORDS_STOP = {}\n",
        model.vocab_size,
        id_or("."), id_or(","), id_or("!"), id_or("?"),
        model.qmark_id, WORD_BUDGET, MIN_WORDS_BEFORE_STOP,
    );
    let consts = format!(
        "{consts}EMERG_W1 = {}\nEMERG_W2 = {}\nSOFT_LEN = {}\nREP_PEN = {}\nLEN_BONUS = {}\nNEURAL_CLAMP = {}\nNEURAL_SH_H = {}\nNEURAL_SH_OUT = {}\n",
        model.emergency.0, model.emergency.1, SOFT_LEN, REP_PEN, LEN_BONUS,
        neural::BASE_CLAMP, model.neural.sh_h, model.neural.sh_out
    );

    let font = font_chr();
    fs::write(out_dir.join("model.bin"), &model_bytes)?;
    fs::write(out_dir.join("tri.bin"), &model.tri)?;
    fs::write(out_dir.join("bi.bin"), &model.bi)?;
    fs::write(out_dir.join("words.bin"), &words_bin)?;
    fs::write(out_dir.join("attr.bin"), &model.attr)?;
    fs::write(out_dir.join("routes.bin"), &model.routes_bytes())?;
    fs::write(out_dir.join("classifier.bin"), &model.cls)?;
    fs::write(out_dir.join("neural.bin"), &model.neural.bank_image())?;
    fs::write(out_dir.join("chr.bin"), &font)?;
    fs::write(out_dir.join("model_consts.inc"), &consts)?;

    let receipt = serde_json::json!({
        "tool": "nano_nes_model_builder/build_model",
        "model_format": "NNW2/MMC5: word-level dense trigram, 16B records of 8x(next,freq), bank=w1>>1 via $5114, record=$8000+(w1&1)*4096+w2*16; bigram fallback in decode bank",
        "corpus_sha256": sha256_hex(&corpus_bytes),
        "vocab_sha256": sha256_hex(&words_bin),
        "model_sha256": sha256_hex(&model_bytes),
        "vocab_size": model.vocab_size,
        "word_count": split_words(&corpus_text).len(),
        "num_trigram_contexts": n_tri,
        "num_bigram_contexts": n_bi,
        "num_route_rows": model.routes.len(),
        "emergency_seed": [model.emergency.0, model.emergency.1],
        "min_answer_words": MIN_ANSWER_WORDS,
        "tokenizer_version": TOKENIZER_VERSION,
        "model_format_version": MODEL_FORMAT_VERSION,
        "neural": {"emb_dim": neural::EMB_DIM, "hid_dim": neural::HID_DIM,
                    "sh_h": model.neural.sh_h, "sh_out": model.neural.sh_out,
                    "trained": "hand-rolled SGD, deterministic LCG init, 350 epochs",
                    "decode": "full-vocab integer argmax on the 6502"},
        "topics": model.topic_names,
        "qtypes": model.qtype_names,
        "scoring": {"base_max": BASE_MAX, "bias_max": BIAS_MAX, "rep_pen": REP_PEN,
                     "len_bonus": LEN_BONUS, "soft_len": SOFT_LEN, "rep_window": REP_WINDOW},
        "top_k": TOP_K,
        "word_budget": WORD_BUDGET,
        "min_words_before_stop": MIN_WORDS_BEFORE_STOP,
        "generation_modes": ["greedy"],
        "rom_mapper": "5 (MMC5), 1 MiB PRG (125 model banks of 8 KiB), 8 KiB CHR-ROM",
        "verification_questions": questions,
    });
    let receipt_str = serde_json::to_string_pretty(&receipt)?;
    fs::write(out_dir.join("model_receipt.json"), &receipt_str)?;
    println!("{receipt_str}");
    Ok(())
}
